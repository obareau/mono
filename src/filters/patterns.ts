import type { Filter } from "./types";

// MacPaint-style pattern dithering. A library of 8x8 1-bit fill tiles (dots, lines,
// weaves, bricks…) in the spirit of the original MacPaint palette. Two modes:
//  - "tone": pick a tile per pixel by local darkness (density-sorted) -> dithered tones.
//  - "single": stamp one chosen tile into the dark areas (a threshold fill).
// Each tile is 8 bytes; bit 7 = leftmost column, a set bit = black ink.

// prettier-ignore
const TILES: number[][] = [
  [0x00,0x00,0x00,0x00,0x00,0x00,0x00,0x00], // white
  [0x80,0x00,0x00,0x00,0x08,0x00,0x00,0x00], // sparse dots
  [0x88,0x00,0x22,0x00,0x88,0x00,0x22,0x00], // fine dots
  [0x88,0x22,0x88,0x22,0x88,0x22,0x88,0x22], // dot grid
  [0xaa,0x00,0xaa,0x00,0xaa,0x00,0xaa,0x00], // horizontal dotted
  [0xaa,0x55,0xaa,0x55,0xaa,0x55,0xaa,0x55], // 50% checker
  [0x55,0xaa,0x55,0xaa,0x55,0xaa,0x55,0xaa], // 50% checker (offset)
  [0x11,0x22,0x44,0x88,0x11,0x22,0x44,0x88], // thin diagonal /
  [0x88,0x44,0x22,0x11,0x88,0x44,0x22,0x11], // thin diagonal \
  [0x33,0x66,0xcc,0x99,0x33,0x66,0xcc,0x99], // thick diagonal /
  [0xcc,0x66,0x33,0x99,0xcc,0x66,0x33,0x99], // thick diagonal \
  [0xff,0x00,0xff,0x00,0xff,0x00,0xff,0x00], // horizontal lines
  [0x88,0x88,0x88,0x88,0x88,0x88,0x88,0x88], // vertical lines
  [0xff,0x88,0x88,0x88,0xff,0x88,0x88,0x88], // grid / mesh
  [0xff,0x80,0x80,0x80,0xff,0x08,0x08,0x08], // bricks
  [0x22,0x55,0x88,0x55,0x22,0x55,0x88,0x55], // weave
  [0x18,0x24,0x42,0x81,0x42,0x24,0x18,0x00], // diamonds
  [0x81,0x42,0x24,0x18,0x18,0x24,0x42,0x81], // crosshatch X
  [0xaa,0xaa,0xaa,0xaa,0xaa,0xaa,0xaa,0xaa], // vertical dotted
  [0xee,0x44,0xee,0x44,0xee,0x44,0xee,0x44], // basket
  [0x77,0x77,0xdd,0xdd,0x77,0x77,0xdd,0xdd], // scales
  [0xdd,0x77,0xdd,0x77,0xdd,0x77,0xdd,0x77], // dense weave
  [0xbb,0xee,0xbb,0xee,0xbb,0xee,0xbb,0xee], // herringbone
  [0xcc,0xcc,0x33,0x33,0xcc,0xcc,0x33,0x33], // big checker
  [0xfe,0xfd,0xfb,0xf7,0xef,0xdf,0xbf,0x7f], // dense diagonal
  [0xee,0xdd,0xbb,0x77,0xee,0xdd,0xbb,0x77], // dense /
  [0x77,0xbb,0xdd,0xee,0x77,0xbb,0xdd,0xee], // dense \
  [0xff,0xaa,0xff,0xaa,0xff,0xaa,0xff,0xaa], // heavy horizontal
  [0xff,0xee,0xff,0xee,0xff,0xee,0xff,0xee], // near-solid lines
  [0xdb,0xdb,0xdb,0xdb,0xdb,0xdb,0xdb,0xdb], // thick vertical bars
  [0xf7,0xff,0x7f,0xff,0xf7,0xff,0x7f,0xff], // near solid + holes
  [0xff,0xff,0xff,0xff,0xff,0xff,0xff,0xff], // black
];

function density(tile: number[]): number {
  let bits = 0;
  for (const b of tile) bits += popcount(b);
  return bits / 64;
}
function popcount(b: number): number {
  let c = 0;
  for (let i = 0; i < 8; i++) c += (b >> i) & 1;
  return c;
}
function bit(tile: number[], x: number, y: number): number {
  return (tile[y & 7] >> (7 - (x & 7))) & 1; // 1 = black
}

// tiles sorted light -> dark, used for tone mapping
const SORTED = [...TILES].sort((a, b) => density(a) - density(b));

export const patterns: Filter = {
  id: "patterns",
  name: "Patterns",
  category: "dither",
  params: [
    { key: "mode", label: "Mode", type: "select", default: "tone", options: ["tone", "single"] },
    { key: "tile", label: "Tile", type: "range", default: 5, min: 0, max: TILES.length - 1, step: 1 },
    { key: "level", label: "Level", type: "range", default: 0.5, min: 0, max: 1, step: 0.01 },
  ],
  apply(gray, w, h, p) {
    const mode = p.mode as string;
    const lastIdx = SORTED.length - 1;
    if (mode === "single") {
      const tile = TILES[Math.min(TILES.length - 1, Math.max(0, Math.round(p.tile as number)))];
      const level = p.level as number;
      for (let y = 0; y < h; y++) {
        for (let x = 0; x < w; x++) {
          const i = y * w + x;
          gray[i] = gray[i] < level && bit(tile, x, y) ? 0 : 1;
        }
      }
      return gray;
    }
    // tone: choose tile by local ink, output its bit at this position
    for (let y = 0; y < h; y++) {
      for (let x = 0; x < w; x++) {
        const i = y * w + x;
        const ink = 1 - gray[i];
        const idx = Math.min(lastIdx, Math.max(0, Math.round(ink * lastIdx)));
        gray[i] = bit(SORTED[idx], x, y) ? 0 : 1;
      }
    }
    return gray;
  },
};
